    private void datos_tbKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_datos_tbKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            int fila = datos_tb.getSelectedRow();
            usuario.setId_sucursal(fila);
            Integer.parseInt(
                datos_tb.getValueAt(fila, 0).toString());
            usuario.setNombre_apellido(datos_tb.getValueAt(fila, 1).toString());
            usuario.setUsuario(datos_tb.getValueAt(fila, 2).toString());
            usuario.setPass(datos_tb.getValueAt(fila, 3).toString());
            Integer.parseInt(
                datos_tb.getValueAt(fila, 4).toString());
            dispose();
        }
    }//GEN-LAST:event_datos_tbKeyPressed

